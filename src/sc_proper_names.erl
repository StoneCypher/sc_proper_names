female_first_name() -> frequency([ {4,"Alice"}, {2,"Betty"}, {1,"Todo"} ]).



framed_sigil() ->
  frequency([
    {80, fun(_Bas) -> ""                   end},
    {10, fun(Base) -> " "  ++ Base         end},
    {3,  fun(Base) -> ", " ++ Base         end},
    {2,  fun(Base) -> " "  ++ Base ++ "."  end},
    {1,  fun(Base) -> ", " ++ Base ++ "."  end},
    {1,  fun(Base) -> " (" ++ Base ++ ")"  end},
    {1,  fun(Base) -> " (" ++ Base ++ ".)" end}
  ]).

sigil() ->
  frequency([
    {120, "Jr"},
    {70,  "Sr"},
    {30,  "III"},
    {10,  "IV"},
    {5,   "Esq"},
    {2,   "Esquire"},
    {1,   "QC"},
    {2,   "Adv"},
    {10,  "MD"},
    {5,   "DDS"},
    {5,   "Ph.D"},
    {5,   "DM"}
  ]).

res_sigil() ->
  ?LET(
    { Frame,          Sig     },
    { framed_sigil(), sigil() },
    Frame(Sig)
  ).



