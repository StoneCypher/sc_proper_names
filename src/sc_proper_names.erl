first_name() ->
  frequency([
    {1, male_first_name()},
    {1, female_first_name()}
  ]).





rep_middle_name() ->
  frequency([
    {300, fun(_M, _S) -> "" end},
    {60,  fun(MN, _S) -> [FirstCh | _] = MN, [FirstCh | [$. | [$  | []]]] end},
    {30,  fun(MN, _S) -> MN ++ " " end},
    {10,  fun(MN, _S) -> [FirstCh | _] = MN, [FirstCh | [$  | []]]  end},

    {1,   fun(MN, SN) ->
      [FirstCh | _] = MN,
      [SecCh   | _] = SN,
      [FirstCh] ++ [SecCh] ++ " " end},

    {1,   fun(MN, SN) ->
      [FirstCh | _] = MN,
      [SecCh   | _] = SN,
      [FirstCh] ++ "." ++ [SecCh] ++ ". " end},

    {1,   fun(MN, SN) ->
      [FirstCh | _] = MN,
      [SecCh   | _] = SN,
      [FirstCh] ++ [SecCh] ++ ". " end}

  ]).

res_middle_name() ->
  ?LET(
    { MiddleName,   MiddleSpare,  MiddleRep         },
    { first_name(), first_name(), rep_middle_name() },
    MiddleRep(MiddleName, MiddleSpare)
  ).





name() ->

  ?LET(
    { RHonorific, First, RMiddle, Last, RSigil },

    { res_honorific(),
      first_name(),
      res_middle_name(),
      last_name(),
      res_sigil() },

    RHonorific ++ First ++ " " ++ RMiddle ++ Last ++ RSigil

  ).

