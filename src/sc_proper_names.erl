last_name() ->

  frequency([
    {29, base_last_name()},
    {1,  hyphenated_last_name()}
  ]).



hyphenated_last_name() ->

  ?LET(
    { FirstName,        SecondName       },
    { base_last_name(), base_last_name() },
    FirstName ++ "-" ++ SecondName
  ).





name_insert() ->
  frequency([
    {12, " " },
    {12, "-" },
    {1,  ""  }
  ]).

double_mens_first_name() ->

  ?LET(
    { FirstName,              Insert,        SecondName             },
    { base_mens_first_name(), name_insert(), base_mens_first_name() },
    FirstName ++ Insert ++ SecondName
  ).

double_womens_first_name() ->

  ?LET(
    { FirstName,                Insert,          SecondName             },
    { base_womens_first_name(), name_insert(), base_womens_first_name() },
    FirstName ++ Insert ++ SecondName
  ).

male_first_name() ->
  frequency([
    {50, base_mens_first_name()   },
    {1,  double_mens_first_name() }
  ]).

female_first_name() ->
  frequency([
    {50, base_womens_first_name()   },
    {1,  double_womens_first_name() }
  ]).



framed_sigil() ->
  frequency([
    {80, fun(_Bas) -> ""                   end},
    {10, fun(Base) -> " "  ++ Base         end},
    {3,  fun(Base) -> ", " ++ Base         end},
    {2,  fun(Base) -> " "  ++ Base ++ "."  end},
    {1,  fun(Base) -> ", " ++ Base ++ "."  end},
    {1,  fun(Base) -> " (" ++ Base ++ ")"  end},
    {1,  fun(Base) -> " (" ++ Base ++ ".)" end}
  ]).

